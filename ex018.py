import math
angulo = float(input('Digite o valor do 창ngulo: '))
seno = math.sin(math.radians(angulo))
print('O 창ngulo de {} tem o SENO {:.2f}'.format(angulo,seno))
cos = math.sin(math.radians(angulo))
print('O 창ngulo de {} tem o COSSENO {:.2f}'.format(angulo,cos))
tan = math.tan(math.radians(angulo))
print('O 창ngulo de {} tem o TANGENTE {:.2f}'.format(angulo,tan))
